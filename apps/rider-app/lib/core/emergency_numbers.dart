/// أرقام الطوارئ الموحّدة حسب رمز الدولة (E.164 country code).
/// الافتراضي 112 (الرقم الأوروبي/الدولي الشائع) عند عدم التطابق.
class EmergencyNumbers {
  EmergencyNumbers._();

  static const Map<String, String> _byCode = {
    '+966': '999', // السعودية
    '+974': '999', // قطر
    '+971': '999', // الإمارات
    '+965': '112', // الكويت
    '+973': '999', // البحرين
    '+968': '9999', // عُمان
    '+20': '122', // مصر
    '+962': '911', // الأردن
    '+1': '911', // أمريكا/كندا
    '+44': '999', // بريطانيا
    '+33': '112', // فرنسا
    '+90': '112', // تركيا
    '+91': '112', // الهند
  };

  /// يعيد رقم الطوارئ لرمز الدولة (يطابق أطول بادئة).
  static String forCountryCode(String? code) {
    if (code == null || code.isEmpty) return '112';
    return _byCode[code] ?? '112';
  }
}
